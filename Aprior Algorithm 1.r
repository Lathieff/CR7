library(Matrix)
library(arules)
library(arulesViz)
library(datasets)
data("Groceries")
summary(Groceries)
length(Groceries)
itemFrequency(Groceries,type="relative")
trules=apriori(Groceries,parameter=list(supp=0.001,conf=0.2,minlen=2))
trules=sort(trules,by="lift",decreasing = TRUE)
inspect(trules[1:20])
plot(trules[1:20],method="graph")
plot(trules[1:20],method="grouped")
plot(trules,measure=c("support","lift"),shading="confidence",jitter=0)
itemFrequencyPlot(Groceries,topN=8, type="relative", ylab="Item Frequency(Relative)")
bcrules=apriori(Groceries,parameter = list(supp=0.001,conf=0.8),appearance = list(default="lhs",rhs=c("whole milk")))
inspect(bcrules[1:20])
plot(bcrules[1:20],method="graph")
plot(bcrules[1:20],method="grouped")
plot(bcrules,measure=c("support","lift"),shading="confidence",jitter=0)
acrules=apriori(Groceries,parameter = list(supp=0.001,conf=0.9),appearance = list(default="lhs",rhs=c("curd","yogurt")))
inspect(acrules)
plot(acrules,method="graph")
plot(acrules,method="grouped")
plot(acrules,measure=c("support","lift"),shading="confidence",jitter=0)